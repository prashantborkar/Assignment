package language_fundamentals;

public class Double_type {
	
	public static void main(String[] args) {
		
		double d =  12_34.5_67_8;  // In double data type putting underscore between digits while compiled program it ignores underscore.
		double d1 = 123245.78;  
		double d2 = 123_456.78_9;
		
		
		
		System.out.println(d+"  "+d1+"  "+d2);
	}

}
